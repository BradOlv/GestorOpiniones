import { Schema, model } from 'mongoose';

const userSchema = Schema({
    nombre: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

export default model('User', userSchema);